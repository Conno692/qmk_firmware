#include QMK_KEYBOARD_H

enum custom_keycodes {
    REBUILD = SAFE_RANGE,
    CHANGE_FILE
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
    case REBUILD:
        if (record->event.pressed) {
            // when keycode REBUILD is pressed
            SEND_STRING(SS_LCTL("Q"));
        } else {
            // when keycode REBUILD is released
        }
        break;
    case CHANGE_FILE:
       if (record->event.pressed) {
            // when keycode REBUILD is pressed
            SEND_STRING(SS_LCTL("KC_TAB"));
        } else {
            // when keycode REBUILD is released
        }
        break; 
    }
    return true;
};

